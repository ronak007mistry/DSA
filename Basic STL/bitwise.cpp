#include<iostream>

using namespace std;

int main() {

    // And 
    cout<<(4&1)<<endl;
    cout<<(3&5)<<endl;

    // Or 
    cout<<(4|1)<<endl;
    cout<<(5|3)<<endl;
    
    // Not  
    cout<<(~5)<<endl;
    cout<<(~1)<<endl;

    // Xor

    cout<<(4^1)<<endl;
    cout<<(5^3)<<endl;
    cout<<(5^0)<<endl;
    cout<<(5^5)<<endl;

    // Left shift

    cout<<(4<<1)<<endl;
    cout<<(2<<2)<<endl;
    cout<<(1<<1)<<endl;

    // Right shift

    cout<<(4<<1)<<endl;
    cout<<(2>>2)<<endl;
    cout<<(1>>1)<<endl;
     

}